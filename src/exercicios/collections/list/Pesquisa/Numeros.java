package exercicios.collections.list.Pesquisa;

public class Numeros {

	// Atributos

	private int numeros;

	// Constructor

	public Numeros(int numeros) {
		this.numeros = numeros;
	}

	// Getters

	public int getNumeros() {
		return numeros;
	}

	// toString

	@Override
	public String toString() {
		return "Numeros [Numeros=" + numeros + "]";
	}

}
